/**
 * Generates a Markdown report of the Clippy analysis results and triggers a download.
 *
 * OUTPUT FORMAT
 * -------------
 * The output is GitHub-compatible Markdown:
 *   - Headers (##, ###, ####)
 *   - Metadata table (Contract, Analyzed, Models, Objectives)
 *   - Per-model sections with trust score, jurisdiction, summary, flag counts
 *   - Dimensions table with ASCII progress bar (█░)
 *   - Flagged clauses as level-4 headers with descriptions and blockquoted quotes
 *   - Legal disclaimer footer
 *
 * The file is created as a Blob in memory and downloaded via a synthetic
 * <a> click. The Blob URL is revoked 2 seconds after click to prevent leaks.
 *
 * @param context - Export context (file name, timestamp, results)
 * @param downloadName - Optional filename for the downloaded Markdown file (without extension)
 */
export function downloadAsMarkdown(context: ExportContext, downloadName?: string): void {
  const lines: string[] = [];
  const date = new Date(context.analyzedAt).toLocaleString();

  // ---------------------------------------------------------------------------
  // Document header
  // ---------------------------------------------------------------------------
  lines.push("# 📎 Clippy — Contract Analysis Report");
  lines.push("");
  lines.push(`> Generated by [Clippy](https://clippy.legal) — open-source AI contract analyzer · *your contract analyst*`);
  lines.push("");

  // Metadata table
  lines.push("| Field | Value |");
  lines.push("| --- | --- |");
  lines.push(`| **Contract** | \`${context.fileName}\` |`);
  lines.push(`| **Analyzed** | ${date} |`);
  lines.push(`| **Models** | ${context.results.map(r => r.modelName).join(", ")} |`);
  if (context.promptTitles.length > 0) {
    lines.push(`| **Objectives** | ${context.promptTitles.join(", ")} |`);
  }
  lines.push("");
  lines.push("> ⚠ **Legal disclaimer:** AI-generated analysis. Not legal advice. Consult a qualified lawyer before acting on any finding.");
  lines.push("");
  lines.push("---");
  lines.push("");

  // ---------------------------------------------------------------------------
  // Per-model sections
  // ---------------------------------------------------------------------------
  for (const result of context.results) {
    lines.push(`## ${result.modelName}`);
    lines.push("");

    if (result.status === "error") {
      lines.push(`> ❌ **Analysis failed:** ${result.error || "Unknown error"}`);
      lines.push("");
      lines.push("---");
      lines.push("");
      continue;
    }

    // Trust score headline with colour-coded emoji
    const scoreEmoji =
      result.trustScore >= 75 ? "🟢" :
      result.trustScore >= 50 ? "🟡" :
      result.trustScore >= 30 ? "🟠" : "🔴";
    lines.push(`### ${scoreEmoji} Trust Score: **${result.trustScore}/100**`);
    lines.push("");

    if (result.jurisdiction && result.jurisdiction !== "Unknown") {
      lines.push(`📍 **Jurisdiction:** ${result.jurisdiction}`);
      lines.push("");
    }

    if (result.durationMs) {
      lines.push(`⏱ **Analysis time:** ${(result.durationMs / 1000).toFixed(1)}s`);
      lines.push("");
    }

    // Summary
    lines.push("### Summary");
    lines.push("");
    lines.push(result.summary);
    lines.push("");

    // Flag counts summary line
    const critCount  = result.flags.filter(f => f.severity === "CRITICAL").length;
    const suspCount  = result.flags.filter(f => f.severity === "SUSPECT").length;
    const minorCount = result.flags.filter(f => f.severity === "MINOR").length;
    lines.push(`🔴 **${critCount} CRITICAL** &nbsp; 🟠 **${suspCount} SUSPECT** &nbsp; 🟡 **${minorCount} MINOR**`);
    lines.push("");

    // Dimensions table with ASCII progress bar
    if (result.dimensions.length > 0) {
      lines.push("### Dimensions");
      lines.push("");
      lines.push("| Dimension | Score | Assessment | Note |");
      lines.push("| --- | --- | --- | --- |");
      for (const dim of result.dimensions) {
        // ASCII progress bar: 10 characters wide, filled proportionally
        const filled = Math.round(dim.score / 10);
        const bar = "█".repeat(filled) + "░".repeat(10 - filled);
        lines.push(`| ${dim.name} | **${dim.score}/100** \`${bar}\` | ${dim.label} | ${dim.note} |`);
      }
      lines.push("");
    }

    // Flagged clauses
    if (result.flags.length === 0) {
      lines.push("### ✅ No Issues Found");
      lines.push("");
      lines.push("No significant problematic clauses were identified. This contract appears fair.");
      lines.push("");
    } else {
      lines.push("### Flagged Clauses");
      lines.push("");

      // Sort by severity: CRITICAL first
      const sorted = [...result.flags].sort((a, b) => {
        const order: Record<string, number> = { CRITICAL: 0, SUSPECT: 1, MINOR: 2 };
        return (order[a.severity] ?? 3) - (order[b.severity] ?? 3);
      });

      for (const flag of sorted) {
        const emoji =
          flag.severity === "CRITICAL" ? "🔴" :
          flag.severity === "SUSPECT"  ? "🟠" : "🟡";
        lines.push(`#### ${emoji} ${flag.severity}: ${flag.title}`);
        lines.push("");
        lines.push(flag.description);
        if (flag.quote) {
          lines.push("");
          lines.push(`> *"${flag.quote}"*`);
        }
        lines.push("");
      }
    }

    lines.push("---");
    lines.push("");
  }

  // ---------------------------------------------------------------------------
  // Footer with legal disclaimer and attribution
  // ---------------------------------------------------------------------------
  lines.push("---");
  lines.push("");
  lines.push("*⚠ This report was generated by [Clippy](https://clippy.legal) — an open-source AI contract analyzer.*");
  lines.push("*Results are AI-generated and **do not constitute legal advice**. Consult a qualified lawyer before acting on any finding.*");
  lines.push("*Results may miss clauses, misinterpret context, or reflect outdated legal standards.*");
  lines.push("");
  lines.push(`*[View Clippy on GitHub](https://github.com/paulfxyz/clippy) · Report generated: ${date}*`);

  // ---------------------------------------------------------------------------
  // Build and trigger download via synthetic <a> click
  // ---------------------------------------------------------------------------
  const md = lines.join("\n");

  // Use UTF-8 charset to support non-Latin content (legal text often has
  // special characters, foreign names, currency symbols, etc.)
  const blob = new Blob([md], { type: "text/markdown;charset=utf-8" });
  const url = URL.createObjectURL(blob);

  // Sanitise the filename: remove characters that are invalid in filenames
  // and limit to 80 characters to avoid OS filename length limits.
  const safeName = (downloadName || context.fileName.replace(/\.[^.]+$/, ""))
    .replace(/[^a-zA-Z0-9_\-. ]/g, "_")
    .slice(0, 80);

  const link = document.createElement("a");
  link.href = url;
  link.download = `${safeName}_clippy_report.md`;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);

  // Revoke the object URL after a short delay to free memory.
  // The delay gives the browser time to start the download before we revoke.
  setTimeout(() => URL.revokeObjectURL(url), 2000);
}
